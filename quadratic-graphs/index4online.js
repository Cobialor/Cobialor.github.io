function mouseDragged() {  
  if ((mouseX < width && mouseX > 0 && mouseY < height) && mouseY > 0 ) {
    let cp1 = [255, 120, 120, 255];
    let cp2 = [255, 180, 120, 255];
    let cp3 = [160, 160, 255, 255];
    let cp4 = [  0, 255, 255, 255];
    if (eqcolor(cm, cp1)) {   
      x1 = mouseX;
      y1 = mouseY;
      p1 = true;
    }
    if (eqcolor(cm, cp2)) {
      x2 = mouseX;
      y2 = mouseY;
      p2 = true;
    }
    if (eqcolor(cm, cp3)) {   
      x3 = mouseX;
      y3 = mouseY;
      p3 = true;
    }
    if (eqcolor(cm, cp4)) {
      x4 = mouseX;
      y4 = mouseY;
      p3 = true;
    }
  }
}

function mousePressed() {
  mouse_down = true;
  draw_allowed = true;
  cm = get(mouseX, mouseY);
}

function mouseReleased() {
  cm = get(mouseX, mouseY);
  mouse_down = false;
  draw_allowed = false;
}

function eqcolor(a, b) {
  return a[0] == b[0] && a[1] == b[1] && a[2] == b[2] && a[3] == b[3];
}

